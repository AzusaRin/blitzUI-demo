<template>
  <div class="collapse-item">
    <div class="title" @click="toggleCollapse">
      {{ title }}
    </div>
    <div class="content" v-if="openStatus===true">
      <slot></slot>
    </div>
  </div>
</template>

<script>
export default {
  name: 'BlitzCollapseItem',
  props: {
    title: {
      type: String,
      required: true
    },
    name:{
      type: String,
      required: true
    }
  },
  data() {
    return {
      openStatus: false,
      single:false
    }
  },
  inject: ['eventHub'],
  methods: {
    toggleCollapse() {
      if (this.openStatus === true) {
        this.openStatus = false
      } else {

      this.eventHub &&  this.eventHub.$emit('update:selected', this.name)
      }
    },
    close() {
      this.openStatus = false
    },
    open(){
      this.openStatus= true
    }
  },
  mounted() {
    this.eventHub &&  this.eventHub.$on('update:selected', (name) => {
      if (name !== this.name) {
        if(this.single){
          this.close()
        }
      }else {
        this.open()
      }
    })
  }
}
</script>

